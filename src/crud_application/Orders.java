    private void Fetch() {
        try {
            int x;
            pst = con.prepareStatement("SELECT * FROM Orders ORDER BY Status DESC");
            rs = pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            x = rss.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)orderstable.getModel();
            df.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for(int a = 1; a <= x; a++) {
                    v2.add(rs.getString("OrderID"));
                    v2.add(rs.getString("OrderUserID"));
                    v2.add(rs.getString("Item"));
                    v2.add(rs.getString("Amount"));
                    v2.add(rs.getString("Status"));
                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Stocks.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
