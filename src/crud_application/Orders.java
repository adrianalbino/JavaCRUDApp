    private void finishorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishorderActionPerformed
        try {
            String OrderID = orderidfield.getText();
            
            pst = con.prepareStatement("UPDATE Orders SET Status=? WHERE OrderID=?");
            pst.setString(1, "Finished");
            pst.setString(2, OrderID);
            
            int x = pst.executeUpdate();
            
            if (x == 1) {
                JOptionPane.showMessageDialog(this, "Order finished!");
                orderidfield.setText("");
                orderidfield.requestFocus();
                Fetch();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Stocks.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_finishorderActionPerformed

